    public SchiedsrichterBearbeitenDialog(java.awt.Frame parent, boolean modal, Schiedsrichter schiedsrichter) {
        super(parent, modal);
        this.schiedsrichter = schiedsrichter;
        initComponents();
        setLocationRelativeTo(parent);
        pack();
        
        txtF_name.setText(schiedsrichter.getName());
        txtF_nachname.setText(schiedsrichter.getNachname());
        if (schiedsrichter.getGeschlecht()== 'm'){
            comboBox_geschlecht.setSelectedIndex(0);
        }
        if (schiedsrichter.getGeschlecht()== 'w'){
            comboBox_geschlecht.setSelectedIndex(1);
        }
        //Geburtstag
        int tag = 0;
        int monat = 0;
        int jahr = 0;
        
        String[] s = schiedsrichter.getGeburtstag().split("\\.");
        combo_sportart1.setModel(ComboBoxModels.getSportarten());
        tag = Integer.parseInt(s[0]);
        monat = Integer.parseInt(s[1]);
        jahr = Integer.parseInt(s[2]);
        comboBox_day.setSelectedItem(tag);
        comboBox_month.setSelectedItem(monat);
        comboBox_year.setSelectedItem(jahr);
        
        sportart = schiedsrichter.getSportart();
        combo_sportart1.setSelectedItem(sportart);
        
    }
    
    public boolean showDialog() {
        this.setVisible(true);
        return didSave;
    }
    
    public void cancel() {
        this.setVisible(false);
        dispose();
        this.didSave = false;
    }
    
    public void save() {
        this.setVisible(false);
        dispose();
        this.didSave = true;
    }
    
    

