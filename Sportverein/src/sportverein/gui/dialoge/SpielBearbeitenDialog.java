    private void button_speichernActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_speichernActionPerformed
        // TODO add your handling code here:
        //

        Integer tag = (Integer) comboBox_day.getSelectedItem();
        Integer monat = (Integer) comboBox_month.getSelectedItem();
        Integer jahr = (Integer) comboBox_year.getSelectedItem();
        String datum = tag + "." + monat + "." + jahr;

        Sportart spo = Verwaltung.getInstance().findSportart(combo_sportart.getSelectedItem().toString());
        Mannschaft m1 = Verwaltung.getInstance().findMannschaft(combo_mannschaft1.getSelectedItem().toString());
        Mannschaft m2 = Verwaltung.getInstance().findMannschaft(combo_mannschaft2.getSelectedItem().toString());

        spiel.setDatum(datum);
        spiel.setHeimMannschaft(m1);
        spiel.setAuswaertsMannschaft(m2);
        spiel.setSportart(spo);
        if (Verwaltung.getInstance().getSpiel().contains(spiel)){
            Verwaltung.getInstance().getSpiel().remove(spiel);
            Verwaltung.getInstance().getSpiel().add(spiel);
            save();
        }
        

    }//GEN-LAST:event_button_speichernActionPerformed

