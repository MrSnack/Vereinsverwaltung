    private void button_speichernActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_speichernActionPerformed
      
       
        schiedsrichter.setName(txtF_name.getText());
        schiedsrichter.setNachname(txtF_nachname.getText());
        if (comboBox_geschlecht.getSelectedIndex()==0){
            schiedsrichter.setGeschlecht('m');
        }
        if (comboBox_geschlecht.getSelectedIndex()==1){
            schiedsrichter.setGeschlecht('w');
        }
        Integer tag = (Integer) comboBox_day.getSelectedItem();
        Integer monat = (Integer) comboBox_month.getSelectedItem();
        Integer jahr = (Integer) comboBox_year.getSelectedItem(); 
        String geburtstag = tag + "." + monat + "." + jahr;
        schiedsrichter.setGeburtstag(geburtstag);
        int alter = Help.alterAusDatumString(geburtstag);
        schiedsrichter.setAlter(alter);
        
        schiedsrichter.setGehalt((double)slider_gehalt.getValue());
        String sportartBez = (String)combo_sportart1.getSelectedItem();
        Sportart spo = Verwaltung.getInstance().findSportart(sportartBez);
        schiedsrichter.setSportart(spo);
        
        if (Verwaltung.getInstance().getSchiedsrichter().contains(schiedsrichter)){
            Verwaltung.getInstance().getSchiedsrichter().remove(schiedsrichter);
            Verwaltung.getInstance().getSchiedsrichter().add(schiedsrichter);
        }
        System.out.println(schiedsrichter.toString());
        save();
        
    }//GEN-LAST:event_button_speichernActionPerformed

